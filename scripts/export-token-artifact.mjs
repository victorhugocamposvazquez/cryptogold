#!/usr/bin/env node
/**
 * Export compiled CryptoGold artifact for backoffice deploy (viem).
 * Run after changing contracts/CappedMintableToken.sol:
 *   npm run contracts:export-artifact
 */
import { readFileSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const artifactPath = join(root, "contracts/out/CappedMintableToken.sol/CappedMintableToken.json");
const outPath = join(root, "lib/token/artifact.ts");

const artifact = JSON.parse(readFileSync(artifactPath, "utf8"));

const contents = `/** Auto-generated by scripts/export-token-artifact.mjs — do not edit */
export const CGOLD_BYTECODE = ${JSON.stringify(artifact.bytecode.object)} as \`0x\${string}\`;
export const CGOLD_ABI = ${JSON.stringify(artifact.abi, null, 2)} as const;
`;

writeFileSync(outPath, contents);
console.log("Exported", outPath);
